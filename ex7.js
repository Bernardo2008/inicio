//Calculadora simples: Escreva uma função que recebe
//dois números e um operador (+, -, *, /) como parâmetros e
//retorna o resultado da operação.

function calculadoraBoa(numero1, numero2) {
    console.log(`${numero1 + numero2}`)
    console.log(`${numero1 - numero2}`)
    console.log(`${numero1 * numero2}`)
    console.log(`${numero1 / numero2}`)
}

calculadoraBoa(15, 3)





